// Ejercicio 09: Validar si una variable tiene un valor asignado


let a;
let b = 11;

if (a === undefined) {
    console.log(`La variable 'a' aun no tiene valor asignado `);
}

a = 'JavaScript'

if (a === undefined) {
    console.log(`La variable 'a' aun no tiene valor asignado `);
} else {
    console.log(`La variable 'a' tiene un valor asignado`);
}

if (b === undefined) {
    console.log(`La variable 'b' aun no tiene valor asignado `);
} else {
    console.log(`La variable 'b' tiene un valor asignado`);
}
